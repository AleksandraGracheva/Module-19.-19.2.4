class Calculator:

   def adding(self, x, y):
       return x + y

   def vstepen(self, x, y):
       return x ** y

   def koren(self, x):
       return x ** 0.5
   def multiply(self,x,y):
       return x * y
   def vichitnie(self,x,y):
       return x-y
   def divchisla(self,x,y):
       return x/y